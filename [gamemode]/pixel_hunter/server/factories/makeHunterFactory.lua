local imports = {
    inventory = exports.pixel_inv,
    notify = exports.pixel_notify
}

HunterZones = {};
HunterZones.cache = {};


function HunterZones:createZone (id, ...)
    if (not HunterZones.cache[id]) then 
        HunterZones.cache[id] = hunter:create(id, ...);
    end

    return HunterZones.cache[id];
end


function HunterZones:update ()
    thread:createHeartbeat(function (this) 
        for _, instance in pairs(HunterZones.cache) do 
            for _, animal in ipairs(instance:getAnimals()) do 
                if (not animal:isDead()) then 
                animal:update(function (state) 
                        if (state == "walk" or state == "run") then 
                            HunterZones:doMovimentAction(animal, state == "run" and true or false);
                        elseif (state == "attack") then
                            HunterZones:doAttackPlayer (animal);
                        elseif (state == "idle" or state == "eating") then
                            HunterZones:doAniamlEat (animal);
                        end
    
                        network:emit("pixel_hunter:setAnimalFactors", true, true, root, {
                            ped = animal.ped, 
                            factors = animal.factors
                        });
                    end);
                end
            end            
        end
    return true end, function () end, 2000)
end


function HunterZones:doMovimentAction (instance, run)
    local x,y,z = instance:getWalkOrRunTo();
    
    if (x and y and z) then 
        network:emit("pixel_hunter:setAnimalWalkTo", true, true, root, instance.ped, run, instance.state, x, y, z);
    end
end


function HunterZones:getZoneByAnimalID (animalId) 
    for _, instance in pairs(HunterZones.cache) do 
        if (instance:getAnimal(animalId)) then 
            return instance;
        end
    end

    return false;
end


function HunterZones:doAttackPlayer (instance) 
    local prey = instance:getPrey()

    if (prey) then 
        local x, y, z = getElementPosition(prey);

        if (x and y and z) then 
            network:emit("pixel_hunter:setAnimalWalkTo", true, true, root, instance.ped, true, instance.state, x, y, z); 
        end
    end
end


function HunterZones:doAniamlEat (instance) 
    if (instance) then 
        network:emit("pixel_hunter:setAnimalWalkTo", true, true, root, instance.ped, false, instance.state);   
    end
end


function HunterZones:onPlayerCollectS (player, data) 
    if (data.transport) then 
        setElementFrozen(player, true);
        setPedAnimation(player, "bomber", "BOM_Plant_Loop", -1, true, false, false);
        network:emit("pixel_hunter:onServerStartCollect", true, false, player, data.transport.id);
    end
end


function HunterZones:onPlayerCollectF (player, data) 
    setElementFrozen(player, false);
    setPedAnimation(player);

    if (not imports.inventory:hasAvailableSlot(player, 3)) then
        return imports.notify:showInfobox(player, "info", "Caçador", "Você não tem espaço suficiente na mochila");
    end

    network:emit("pixel_hunter:onPlayerCollect", true, false, player, data);
    imports.inventory:givePlayerItem (player, data.transport.id, 3, 1, false, false, {}, "bag", true);
end

function HunterZones:onAnimalWasted (killer) 
    local animalId = getElementData(source, "hunters:animal_id");
    if (not animalId) then return false end

    local instance = HunterZones:getZoneByAnimalID (animalId);
    if (not animalId) then return false end

    -- Deploy Respawn Animal Instance
    instance:onAnimalWasted(animalId, killer, function () 
        network:emit("pixel_hunter:onServerClean", true, false, root, animalId);
    end);

    network:emit("pixel_hunter:onPlayerKillAnimal", true, false, killer, animalId);
end
